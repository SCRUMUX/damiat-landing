#!/usr/bin/env node
/**
 * Phase 10 task 10.12 — Figma plugin manifest validation.
 *
 * Reads `ai-manifest.json` and rewrites the AICADS_MANIFEST block inside
 * `figma-plugin/figma-ai-ds-code.js` between the AICADS_MANIFEST_BEGIN /
 * AICADS_MANIFEST_END markers. This keeps the Figma plugin's view of valid
 * component roles in lock-step with the canonical manifest without requiring
 * runtime fetch (which would force `networkAccess` in the plugin manifest).
 *
 * Usage:
 *   node scripts/embed-figma-manifest.mjs
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, '..');

const manifestPath = join(ROOT, 'ai-manifest.json');
const patternsPath = join(ROOT, 'ai-patterns.json');
const pluginPath = join(ROOT, 'figma-plugin', 'figma-ai-ds-code.js');

const manifest = JSON.parse(readFileSync(manifestPath, 'utf8'));
let patternsPayload = { patterns: [], pageTemplates: [] };
try {
  const patterns = JSON.parse(readFileSync(patternsPath, 'utf8'));
  patternsPayload = {
    patterns: Object.keys(patterns.patterns || {}).sort(),
    pageTemplates: Object.keys(patterns.pageTemplates || {}).sort(),
  };
} catch {
  // ai-patterns.json optional for older checkouts
}

const components = manifest.components || {};
const componentNames = Object.keys(components).sort();
const roles = [...new Set(componentNames.map((n) => components[n]?.role).filter(Boolean))].sort();

const payload = {
  version: manifest.version,
  components: componentNames,
  roles,
  ...patternsPayload,
};

const block = [
  '// >>> AICADS_MANIFEST_BEGIN (auto-generated by scripts/embed-figma-manifest.mjs — do not edit by hand)',
  'var AICADS_MANIFEST = ' + JSON.stringify(payload, null, 2) + ';',
  '// <<< AICADS_MANIFEST_END',
].join('\n');

const src = readFileSync(pluginPath, 'utf8');
const re = /\/\/ >>> AICADS_MANIFEST_BEGIN[\s\S]*?\/\/ <<< AICADS_MANIFEST_END/;

let next;
if (re.test(src)) {
  next = src.replace(re, block);
} else {
  // First-time install: inject right after `figma.showUI(...)`.
  next = src.replace(
    /(figma\.showUI\([^;]+;\s*)/,
    `$1\n${block}\n`,
  );
}

if (next === src) {
  console.error('embed-figma-manifest: could not locate insertion point');
  process.exit(1);
}

writeFileSync(pluginPath, next, 'utf8');

console.log(
  `embed-figma-manifest: embedded ${componentNames.length} components / ${roles.length} roles ` +
  `(manifest version ${manifest.version})`,
);
